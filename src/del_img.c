#include "fdf.h"

void	del_img(t_win *win)
{
	int x;
	int	y;

	y = -1;
	while (++y < HEIGHT)
	{
		x = -1;
		while (++x < WIDTH)
			((int *)win->img)[y * WIDTH + x] = 0x000000;
	}
}
